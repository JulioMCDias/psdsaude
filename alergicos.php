                    <h4>Tratamentos</h4>
                    <p>Procura-se aliviar os sintomas das reações alérgicas em desenvolvimento e evitar o aparecimento de novas reações. Os corticóides diminuem a resposta imunológica e aliviam os sintomas. A epinefrina reduz a inflamação das vias aéreas e a difenidramina (um anti-histamínico) alivia os sintomas. Para evitar a recidiva das reações alérgicas, é indispensável impedir o consumo ou o contato com a substância que as produz.
                    Também existem tratamentos para eliminar a sensibilidade, baseados em inoculações regulares de quantidades crescentes do alérgeno – vacinas imunoalérgicas.</p>
                                        
                    <p><strong>Incidência e Geografia das alergias</strong><br>
                        As alergias e os alérgicos são cada vez em maior número . Cerca de 25% da população dos países industrializados sofrem algum tipo de alergia. No Brasil as alergias mais comuns são as rinites alérgicas, asma e as alergias da pele.
                        As alergias da pele também têm aumentado muito, principalmente entre as donas de casa e os trabalhadores.
                        Cerca de 80% dos casos de asma infantil são causadas por alergia. Entre os adultos a taxa é de 50%. Demonstrou-se que ocorre uma progressão dos sintomas alérgicos, ou seja, crianças alérgicas com sintomas nasais (rinite) freqüentemente desenvolvem asma à medida que crescem.</p>
                                       
