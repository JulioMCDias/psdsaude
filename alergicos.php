                <p><strong>De que maneira se desenvolve a alergia?</strong><br>
                A alergia não se pega, ela é causada por um defeito no sistema imunológico. Exceto na dermatite de contato, a alergia é causada por uma produção desnecessária de anticorpos IgE contra substâncias naturais do meio ambiente, como por exemplo os ácaros da poeira doméstica e os pólens, que passam a agir como alérgenos.</p>

                <p><strong>Quais são os processos de sensibilização?</strong><br>
                O sistema imunológico da pessoa reage contra os alérgenos da mesma forma que deveria reagir contra algum agente agressor à sua saúde. A razão de o Sistema Imunológico identificar componentes inócuos ainda é desconhecida, mas você só pode se tornar alérgico a alguma substância se você foi exposto a ela antes, num processo chamado de sensibilização.
                Você tem maior risco de desenvolver uma alergia dependendo da herança genética e do meio ambiente.</p>

                <p><strong>Vejamos ambas as situações:</strong><br>
                Herança genética, ainda não é bem compreendida, mas pode desenvolver a alergia quando ambos os pais são alérgicos ou um de seus pais ou irmão(ã) já é alérgico.
                Meio ambiente, quando se está exposto a grande quantidade de poeira doméstica, em especial em bebês devido ao uso de cortinas, pelúcia e carpetes; fumaça de cigarros, em especial na infância; e alérgenos tipo ácaros, baratas, mofo, etc.</p>

