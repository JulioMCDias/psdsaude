                        <p><strong>Causas</strong><br>
                        A hipersensibilidade do sistema imunológico gera respostas excessivas diante da presença de substâncias inofensivas denominadas alérgenos que, via de regra, não produzem nenhuma reação na maioria das pessoas.
                        A primeira exposição ao alérgeno produz uma resposta imunológica leve, que sensibiliza o sistema para essa substância específica.
                        A segunda exposição ao alérgeno dá origem a uma série de sintomas que vão depender do tipo de substância, do lugar do organismo exposto ao mesmo e das características da resposta imunológica.
                        Quando o alérgeno entra no corpo de uma pessoa cujo sistema imunológico está sensibilizado, ocorre a produção de anticorpos e são liberadas a histamina e outras substâncias químicas diretamente responsáveis pelos sintomas.
                        Além dos alérgenos comuns (pólen, poeira, bolor, etc), os cosméticos, as jóias, as mordidas de insetos e quase qualquer substância que entre em contato com o corpo podem ser responsáveis pela reação alérgica. Entretanto, certos estímulos físicos, como o frio, o calor, a luz solar ou o atrito, podem provocar os sintomas.</p>
                    
                    <p><strong>Sintomas</strong><br>
                        Os tecidos afetados podem apresentar inchaço, prurido, produção de muco, espasmos musculares, urticária ou outro tipo de erupções. A característica particular de cada quadro dependerá da região do corpo exposta ao alérgeno.
                        Quando entra com o ar inspirado, produzirá congestão nasal, aumento do muco, sensação de coceira no nariz, nos olhos e na garganta, tosse, espirros, lacrimejamento e dificuldade para respirar.
                        Quando o alérgeno chega pelos alimentos, o paciente será acometido por dores e cãibras abdominais, numa primeira fase. Quando o alérgeno entra no fluxo sanguíneo, depois de ser absorvido no intestino delgado, podem ocorrer vermelhidão da pele, urticária, vômitos, diarréia e dor de cabeça.
                        Diagnóstico
                        Antes de tudo, é importante analisar o panorama do quadro alérgico: o momento no qual os sintomas ocorrem (horário, estação do ano), as possíveis mudanças na dieta, a presença de animais de estimação em casa, etc. Em certos casos, é necessário confirmar a presença de uma reação alérgica, determinando com uma análise de sangue a concentração de imunoglobulina E e os níveis do complemento (os dois encontram-se aumentados nas reações alérgicas). Também podem ser feitos os testes correspondentes para identificar o alérgeno que está produzindo a reação: a inoculação intradérmica, a instilação na conjuntiva do olho ou outra técnica adequada.</p>
