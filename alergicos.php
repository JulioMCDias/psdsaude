                    <p>O tratamento de doenças alérgicas baseia-se em evitar o alérgico, farmacoterapia, imunoterapia e educação do paciente.</p>
                    
                    <p><strong>O que é alérgeno?</strong><br>
                    Trata-se de substâncias de origem natural (ambientais ou alimentares), geralmente proteínas, que ao entrarem em contato com um paciente alérgico causam o desenvolvimento de reações caracterizadas por inflamações no órgão sensível, que pode ser o nariz, o pulmão ou a pele.</p>
                    
                    <p><strong>O que é alergia?</strong><br>
                    Alergia é o conjunto de sintomas causados pela resposta imune diante de substâncias que não produzem esse tipo de reação na maioria das pessoas. Abrange uma extensa série de quadros, dependendo de as substâncias serem alimentos, pólen, descamações da pele de animais ou drogas.                    </p>
